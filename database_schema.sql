    ijazah_tertinggi TEXT,
    gaji TEXT,
    tunjangan TEXT,
    uang_makan TEXT,
    honor TEXT,
    lembur TEXT,
    pendapatan_lain TEXT,
    total_pendapatan TEXT,
    pendapatan_usaha TEXT,
    pendapatan_passive TEXT,
    profesi TEXT,
    status_pekerjaan TEXT,
    disabilitas_fisik TEXT,
    disabilitas_mental TEXT,
    disabilitas_intelektual TEXT,
    disabilitas_netra TEXT,
    disabilitas_rungu TEXT,
    disabilitas_wicara TEXT,
    hipertensi TEXT,
    rematik TEXT,
    asma TEXT,
    jantung TEXT,
    diabetes TEXT,
    tbc TEXT,
    stroke TEXT,
    kanker TEXT,
